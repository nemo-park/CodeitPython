# 03. 문자열

print("Python")

# 구분문자 (Delimiter) : 독립된 문자열을 구분해주는 문자
print("I'm excited to learn Python!")

print("I\'m \"excited\" to learn Python!")

print("Hello" + " World")

# 문자열 * n은 문자열을 n번 연속 붙인다는 의미
# n이 양수일때 정상 동작
print("Hello " * 3)
# n이 양수가 아닌경우 빈 문자열을 Return
print("Hello " * 0)
print("Hello " * -2)

print("3" + "5")